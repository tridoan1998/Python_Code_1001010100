def main():
    randomnumber = 35
    found = False
    while not found:
        userinput = int(input("Enter the number here: "))
        if userinput == randomnumber:
           found = False
        else:
            found = True
    print("Congrat! You won the game.")

if __name__ == "__main__":
    main()







